def Spectr(s):
	out = []
	out.append(0)
	k = len(s)
	for i in range(1, k):
		for j in range(k):
			if j+i<=k:
				out.append(mass(s[j:j+i]))
			else:
				out.append(mass(s[j:k]+s[0:j+i-k]))
	out.append(mass(s))
	out.sort()
	sout = ''
	for i in out:
		sout = sout + str(i) + ' '
	return sout

